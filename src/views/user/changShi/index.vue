			       <p>喂养小香猪的时候需要住在安静干燥的环境中，温度在25度左右最好，平时可以让它住在书房，或者是阳台都是可以的，宠物猪很怕冷，冬天的时候一定要记得给它准备一个温暖的窝，加些毛毯或者是被子，不要让它们着凉感冒或者是拉肚子，对小香猪的健康都是很不利的，如果小香猪出现了咳嗽，眼睛，鼻子流出粘液性分泌物，呼吸急促的话，要尽快把小香猪送到宠物医院就医，医生会根据小香猪的具体情况，控制小香猪的病情，带回家的时候也要仔细观察两天，千万不要自己私自买药给小香猪喂食，避免造成更大的伤害。</p>
