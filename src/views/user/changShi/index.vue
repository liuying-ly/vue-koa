			    <img src="@/assets/img/dog1.jpg" alt=""  width="20%" align="right"/>
			    <p>有些家长认为，如给狗狗全部喂肉类，可以使狗狗长得健壮，其实这是错的做法。全吃肉类不只不能使狗狗健壮，反而因消化不良，难以吸收而使多数犬发生腹泻。肉类中蛋白质虽多，但维生素A、D、E和碘较少，同时肉类中钙少磷多，长期吃肉，易造成幼犬体内因钙、磷比例失调而骨骼形成障碍，易于断裂或出现跛行，对大型犬等更不宜过早喂以多量肉类，否则可能引起前肢弯曲。</p>
