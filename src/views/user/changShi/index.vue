			    <img src="@/assets/img/bird1.jpg" alt=""  width="30%" align="left"/>
			    <p>食罐和水罐：食、水罐是养鸟必备的器具，也是一种装饰。常见的有瓷罐、塑料罐、金属罐、竹罐等。除中、大型鹦鹉类的鸟用金属罐外，其他的鸟一般都用美观大方的瓷罐，鸟舍中多用结实耐用、不怕摔的塑料罐，以及大盆、盘或食槽、水槽等。
			    </p><br /><br /><i>巢箱：</i><br />
			    <p>用草绳编制。巢形主要有2种：一种是壶形巢，供文鸟类及营暗巢的小鸟使用；
			    另一种是碗形巢，供雀类等营开口巢的小鸟使用。此外，还有用三合板做的像小房子形状的
			    </p><i>筛子：</i><br />
			    <p>用以筛选散落笼底和食罐中吃剩的粒料。筛子有大眼和小眼两种，大眼筛子用于筛除鸟粪、垃圾、杂草和籽壳，小眼筛子主要是筛除泥沙。自制可在马口铁或铝皮上用钉打眼，或用铜纱布，外面加上木框即可。
			    </p><i>水壶：</i><br />
			    <p>专门用于向鸟笼的水罐中加水的用具。常用壶嘴细长的水壶，能伸进鸟笼到水罐口上，不会使水洒在笼内。可以用铁皮、铝皮等制作，也可以用旧塑料罐改制。在水壶上套上一个喷头，可以给鸟淋浴。小型鸟笼可用塑料眼药水瓶作加水器，还能吸出水罐内的污水
			    </p><i>栖杠：</i><br />
			    <p>也叫栖木，是鸟栖息的支架。最好的材料是天然去皮树根，其次是树支，以圆形略扁最适合鸟抓握。栖杠的粗细要根据鸟的体型大小来确定，太粗或太细都不适宜，以鸟爪抓握后，前后趾爪之间还有少量距离为宜。栖杠的表面要粗糙，便于鸟站立。如果用竹枝，需要在上面刻纹。有的栖杠上粘一层金刚沙，使鸟能研磨嘴和爪，这种栖杠多用于画眉、八哥笼内。大、中型的鸟笼可设置2根栖木，一高一低。有的设置可以晃动的浪木，可以使鸟增加活动量。
			    每天除了给观赏鸟供给合理搭配的日料和新鲜干净的饮水外，日常的管理在保持鸟的健康、正常生长发育和繁殖上也具有非常重要的意义，必须给予应有的重视。如若饲养管理不善，轻则将使鸟儿发病，重则可致导致鸟儿的死亡。每日应首先观察鸟的健康情况，看看活动、采食、饮水、粪便有无异常，如果发现异常情况，应立即多方寻找原因，及时采取措施。每天洗笼、洗罐后不要忘记添加饲料。有些带壳的饲料，经观赏鸟剥食后，籽壳留在食罐里，会出现仅有籽壳而无饲料的情况，所以要养成勤查食罐的习惯。
			    </p>
